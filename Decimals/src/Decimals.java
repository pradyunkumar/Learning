public class Decimals {

    public static void main(String[] args){

        int theint = 5;
        float thefloat = 5.49f;  //add f(casting) to the end of number to denote float
        double thedouble = 5.37d;    //no need for d at end, it is assumed
        //width of int is 32 (4 bytes)
        //width of float is 32 (4 bytes)
        //width of double is 64 (8 bytes)
    }
    
}
